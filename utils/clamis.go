package utils

import (
	"github.com/gin-gonic/gin"
	uuid "github.com/satori/go.uuid"
	"oplian/define"
	"oplian/global"
	systemReq "oplian/model/system/request"
)

func GetClaims(c *gin.Context) (*systemReq.CustomClaims, error) {
	token := c.Request.Header.Get(define.TOKEN_NAME)
	j := NewJWT()
	claims, err := j.ParseToken(token)
	if err != nil {
		global.ZC_LOG.Error("Failed to retrieve the parsing information from the jwt from Gin Context. Check the request header for the presence of an x-token and whether the claims are structured as specified")
	}
	return claims, err
}

// GetUserID User ID
func GetUserID(c *gin.Context) uint {
	if claims, exists := c.Get("claims"); !exists {
		if cl, err := GetClaims(c); err != nil {
			return 0
		} else {
			return cl.ID
		}
	} else {
		waitUse := claims.(*systemReq.CustomClaims)
		return waitUse.ID
	}
}

